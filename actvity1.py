num1=int(input("enter a number "))
num2=int(input("enter  another number "))


print("Bitwise OR of {} and {} is {}".format(num1,num2,num1|num2))
                                            
print("Bitwise AND of {} and {} is {}".format(num1,num2,num1&num2))
print("Bitwise XOR of {} and {} is {}".format(num1,num2,num1^num2))
print("Bitwise NOT of {} and {} is {}".format(num1,num2,~num1,~num2))

